public class MDAS {
    public static void main (String[] args) {
    	int num1 = 5;
    	int num2 = 10;
    	int prod, sum, diff;
    	double quot;
    	
    	prod = num1 * num2;
    	sum = num1 + num2;
    	diff = num1 - num2;
    	quot = (double) num1 / num2; 
    		
    	System.out.println("The given numbers are " + num1 + " and " + num2 + " respectively.\n" );
    	System.out.println("The prod is: " + prod);
    	System.out.println("The sum is: " + sum);
    	System.out.println("The diff is: " + diff);
    	System.out.println("The qout is: " + quot);
    	
 }
}